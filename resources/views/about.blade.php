      <p>Because modern seed stocks are developed with an eye to higher yields, crops are often less nutritious than lower-yielding heirlooms.</p>
      <h4>Open Pollination</h4>
      <p>Heirloom seeds generally propagate naturally, whereas modern hybrid seed stocks often perform poorly..</p>
      <h4>Greater Variety</h4>
      <p>While modern seed crops are generally bred for uniform ripening, which benefits comercial haresting.  Heirloom seeds, by contrast, will often produce throughout a longer growing season.</p>
      <h4>Affordability</h4>
      <p>Genetically modified (GMO) and other modern hybrid seeds are often proprietary commercial products, costing more than similar heirloom seeds.</p>
